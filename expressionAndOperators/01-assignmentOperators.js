//? ASSIGNMENT OPERATOR:
let r = 5;
// our equal sign is the assignment

//?ADDITION
r += 1;      // r = r + 1, or 5 + 1 because r has been set to 5
console.log(r);

let one = 'hum';
one += 'drum';
console.log(one);
one += 'change';
console.log(one);

//?SUBTRACTION
r -= 2;    // r = r - 2. the previous value (after addition above for r, was 6)
console.log(r);


//?MULTIPLICATION
r *= 2;
console.log(r);

//?DIVISION
r /= 1;
console.log(r);

//?EXPONENTIAL
r **= 2;   // squaring a number
console.log(r);

//?REMAINDER
r %= 1;
console.log(r);